lista = [5, 2, 1, 4, 3]
#se utiliza para contar cuantas veces hay un dato en especifico en una lista
print(lista.count(5))
#para encontrar un dato en especifico en la lista, indica la posicion en la que esta
print(lista.index(4))
print(lista)
#metodo para ordenar la lista
lista.sort()
print(lista)
#para cambiar el orden de la lista, que sea al reves
lista.reverse()
print(lista)


