print("Ingrese una Vocal")
Vocal = str(input())

if Vocal.lower() == "a":
    print("Esta vocal es la 'A'")
elif Vocal.lower() == "e":
    print("Esta vocal es la 'E'")
elif Vocal.lower() == "i":
    print("Esta vocal es la 'I'")
elif Vocal.lower() == "o":
    print("Esta vocal es la 'O'")
elif Vocal.lower() == "u":
    print("Esta vocal es la 'U'")
else:
    print("No es una vocal")
